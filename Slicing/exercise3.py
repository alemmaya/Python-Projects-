"""---------------Exercise 3: String Slicing----------------------
When slicing strings, use the following syntax: my_string[start : stop]
-----------------------------------------------------------------------"""

# (1) Given the variables below, print the desired slice
basket_one = 'Green Yellow'
basket_two = 'Blues Jazz'
basket_three = 'Rock Country'

# Slice 1 OUTPUT: Green
print(basket_one[0:6])
print(basket_two[6:11])
print(basket_three[5:12])

# Slice 2 OUTPUT: Jazz

# Slice 3 OUTPUT: Country

